/*Какие из перечисленных ниже alert выполнятся?

    Какие конкретно значения будут результатами выражений в условиях if(...)?

 */

if (-1 || 0) alert( 'first' ); // выполнится. результат в if: true;
if (-1 && 0) alert( 'second' ); // не выполнится. результат в if: false
if (null || -1 && 1) alert( 'third' ); // выполнится. результат в if: true;